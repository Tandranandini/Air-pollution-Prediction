import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.preprocessing import MinMaxScaler
from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_squared_error

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout

df = pd.read_excel("mergedata (1) (1).xlsx")

df['Timestamp'] = pd.to_datetime(df['Timestamp'])

df = df.sort_values('Timestamp')

print(df.head())

data = df[['PM2.5 (µg/m³)']]

scaler = MinMaxScaler()
scaled_data = scaler.fit_transform(data)

sequence_length = 30

X = []
y = []

for i in range(sequence_length, len(scaled_data)):
    X.append(scaled_data[i-sequence_length:i])
    y.append(scaled_data[i])

X = np.array(X)
y = np.array(y)

print(X.shape)

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.2, shuffle=False
)

model = Sequential()

model.add(LSTM(64, return_sequences=True, input_shape=(X_train.shape[1], X_train.shape[2])))
model.add(LSTM(32))
model.add(Dropout(0.2))
model.add(Dense(1))

model.compile(optimizer='adam', loss='mse')

model.fit(X_train, y_train, epochs=50, batch_size=32)

test_predictions = model.predict(X_test)

y_test_actual = scaler.inverse_transform(y_test)
y_pred_actual = scaler.inverse_transform(test_predictions)

mse = mean_squared_error(y_test_actual, y_pred_actual)
rmse = np.sqrt(mse)

print("MSE:", mse)
print("RMSE:", rmse)

plt.figure(figsize=(6,6))

plt.scatter(y_test_actual, y_pred_actual, alpha=0.5)

z = np.polyfit(y_test_actual.flatten(), y_pred_actual.flatten(), 1)
p = np.poly1d(z)

plt.plot(y_test_actual, p(y_test_actual))

plt.xlabel("Actual")
